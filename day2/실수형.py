height = 160.5
weight = 45.6
print(height)
print(weight)

# 실수의 연산
print(height - weight)

# 실수와 실수의 연산은 소수점 이하의 값까지 연산
x = 2.2
y = 2.8
print(x + y)

# 실수와 정수를 연산하면 결과는 실수 테이터
first = 3.9
second = 3
print(first * second)

# 소수점 .0을 포함한 숫자는 실수로 간주한다.
seven = 7.0
three = 3.0
print(seven + three)

print(float(1)) # 1.0
print(float(True)) # 1.0
print(float(False)) # 0.0
print(float('3.14')) # 3.14
print(float('100')) # 100.0



